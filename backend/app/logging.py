import logging


# TODO: You can log to stderr with a config setting
def setup_logging():
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s - %(name)s - %(levelname)s - %(message)s",
        datefmt="%Y-%m-%d %H:%M:%S",
    )
    logging.getLogger('sqlalchemy.engine').setLevel(logging.INFO)
